"""VLM-as-judge scoring: an open VLM (loaded locally via transformers, same
as the models under test) compares the ground-truth diagram image against
the rendered PNG of a model's generated TikZ and returns a structured score.

Loaded once per scoring run (see JudgeRunner) and reused across every
sample -- not reloaded per comparison, since nothing about the judge model
changes between samples.
"""
from __future__ import annotations

import json
import re
from dataclasses import dataclass
from pathlib import Path

import torch
from PIL import Image
from transformers import AutoModelForImageTextToText, AutoProcessor

JUDGE_PROMPT = """You are an expert judge comparing two images of a technical \
architecture/pipeline diagram:
- IMAGE A: the ground-truth original diagram.
- IMAGE B: a reproduction generated by an AI model from IMAGE A.

Score how well IMAGE B reproduces IMAGE A on a 1-5 integer scale:
5 = near-perfect match (layout, shapes, text, connections all correct)
4 = minor differences (small layout/text issues, overall very faithful)
3 = moderate differences (some elements missing/wrong, but recognizably the same diagram)
2 = major differences (significant missing/incorrect structure, barely recognizable)
1 = unrelated or fails to reproduce the diagram at all

Respond ONLY with a JSON object: {"score": <1-5 integer>, "rationale": "<one sentence>"}
"""


@dataclass
class JudgeResult:
    score: int | None
    rationale: str
    raw_output: str


def _parse_judge_output(raw_output: str) -> tuple[int | None, str]:
    match = re.search(r"\{.*\}", raw_output, re.DOTALL)
    if not match:
        return None, f"could not parse judge output: {raw_output[:200]}"
    try:
        data = json.loads(match.group(0))
        score = int(data["score"])
        if not (1 <= score <= 5):
            return None, f"score out of range: {score}"
        return score, data.get("rationale", "")
    except (json.JSONDecodeError, KeyError, ValueError, TypeError):
        return None, f"could not parse judge output: {raw_output[:200]}"


class JudgeRunner:
    def __init__(self, hf_repo: str, trust_remote_code: bool = False,
                 attn_implementation: str = "sdpa", device: str = "cuda"):
        self.device = device
        self.processor = AutoProcessor.from_pretrained(hf_repo, trust_remote_code=trust_remote_code)
        self.model = AutoModelForImageTextToText.from_pretrained(
            hf_repo,
            trust_remote_code=trust_remote_code,
            attn_implementation=attn_implementation,
            dtype=torch.bfloat16,
        ).to(device).eval()

    def unload(self):
        del self.model
        torch.cuda.empty_cache()

    @torch.no_grad()
    def judge(self, gt_image_path: Path, rendered_image_path: Path, max_new_tokens: int = 300) -> JudgeResult:
        gt_image = Image.open(gt_image_path).convert("RGB")
        rendered_image = Image.open(rendered_image_path).convert("RGB")
        messages = [
            {
                "role": "user",
                "content": [
                    {"type": "text", "text": JUDGE_PROMPT},
                    {"type": "text", "text": "IMAGE A (ground truth):"},
                    {"type": "image", "image": gt_image},
                    {"type": "text", "text": "IMAGE B (reproduction):"},
                    {"type": "image", "image": rendered_image},
                ],
            }
        ]
        inputs = self.processor.apply_chat_template(
            messages,
            add_generation_prompt=True,
            tokenize=True,
            return_dict=True,
            return_tensors="pt",
        ).to(self.device)

        input_len = inputs["input_ids"].shape[-1]
        output_ids = self.model.generate(**inputs, max_new_tokens=max_new_tokens, do_sample=False)
        new_tokens = output_ids[0][input_len:]
        raw_output = self.processor.decode(new_tokens, skip_special_tokens=True)

        score, rationale = _parse_judge_output(raw_output)
        return JudgeResult(score=score, rationale=rationale, raw_output=raw_output)
